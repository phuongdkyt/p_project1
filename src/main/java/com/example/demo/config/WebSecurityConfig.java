import com.example.demo.services.UserDetailsServiceImpl;
import com.example.demo.services.oauth2.CustomOAuth2UserService;
import com.example.demo.services.oauth2.HttpCookieOAuth2AuthorizationRequestRepository;
import com.example.demo.services.oauth2.OAuth2AuthenticationFailureHandler;
import com.example.demo.services.oauth2.OAuth2AuthenticationSuccessHandler;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
